package com.web.model;

public class WebsiteForm {
	private int id;
	
	private String address;
	
	private int isCanReptile;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}

	/**
	 * @return the isCanReptile
	 */
	public int getIsCanReptile() {
		return isCanReptile;
	}

	/**
	 * @param isCanReptile the isCanReptile to set
	 */
	public void setIsCanReptile(int isCanReptile) {
		this.isCanReptile = isCanReptile;
	}
	
	
	
	
}
